#площадь круга:

pi = 3.1415 #число пи
r = 5 #радиус
sq_round = pi * r ** 2
print('площадь круга:', round(sq_round, 2))

#длина окружности:
len_r = 2 * pi * r
print('длина окружности:', round(len_r, 2))

